public class CWH_04_literals {
    public static void main(String[] args) {
        byte age = 34;
        int age2 = 56;
        short age3 = 87;

        // long is defined by adding a L/l keyword
        long ageDino = 5666666666666L;
        char ch = 'A';

        /* In java a float is not pre defined we need to define it using a F/f keyword */
        float f1 = 5.6f;
        double d1 = 4.66;

        boolean a = true;
        System.out.println(age);
        String str = "Sourav";
        System.out.println(str);

    }
}
